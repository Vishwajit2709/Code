class CarModel: #classname
    name = "BMW" #variable
    gear = 5
    def run(self):   #functions
        print("Car gear is:- ", self.gear)
        print("car name is:- ", self.name)
    def gear1(self):
        print("car gear is:- ", self.gear)
car_obj = CarModel() #create the the object
print(car_obj.name)
car_obj.run() #with the help object, call the function.
car_obj.gear1()

